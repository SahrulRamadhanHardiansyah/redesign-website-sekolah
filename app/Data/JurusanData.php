    public static function findBySlug($slug)
    {
        $jurusans = self::all();
        foreach ($jurusans as $jurusan) {
            if ($jurusan['slug'] === $slug) {
                return $jurusan;
            }
        }
        return null;
    }

    public static function getAllForListing()
    {
        return array_map(function($jurusan) {
            return [
                'slug' => $jurusan['slug'],
                'nama' => $jurusan['nama'],
                'logo' => $jurusan['logo'],
                'deskripsi' => substr($jurusan['deskripsi'], 0, 150) . '...',
            ];
        }, self::all());
    }
